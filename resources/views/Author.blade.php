          <p class="lead">
            <font size="7" class="">張文環</font>（1909─1978）</p>
        </div>
      </div>
      <div class="row">
        <div class="col-md-12">
          <font size="5">
            <p class="">
              <img src="{{ URL::asset('/Author.jpg') }}" style="float: right; margin: 10px;"> 
                    1927年赴日就學。1932年與林兌、吳坤煌等組「文化サ－ワル」，九月遭日警取締，而從日本東洋大學文學部輟學。1933年與吳坤煌、王白淵、巫永福、蘇維熊、施學習、陳兆柏、楊基振、曾石火等人組織「台灣藝術研究會」，發行純文學雜誌《福爾摩沙》，發表小說〈早凋的蓓蕾〉。<br>
                    1934年《福爾摩沙》第二號出刊，發表小說〈貞操〉。1935年，張文環以小說〈父親的顏面〉入選《中央公論》小說徵文第四名，之後為台灣文藝聯盟東京分盟活躍份子。二月，參加「台灣文藝聯盟東京支部」第一回茶會，發表小說〈自己的壞話〉、〈哭泣的女人〉、〈父親的要求〉、〈重荷〉。<br>
                    1936年， 發表小說〈部落的元老〉及隨筆〈被迫用上的題目〉。1937年，與定兼波子（張芙美）結婚。發表小說〈豬的生產〉。<br>
                    1938年返臺擔任《風月報》編輯、翻譯徐坤泉大眾小說名著《可愛的仇人》，並任職於台灣映畫株式會社；發表評論〈文章與生活〉、散文〈先覺者的悲哀〉、小說〈兩個新娘〉。<br>
                    1940年發表長篇小說〈山茶花〉連載於《台灣新民報》；評論〈台灣文學的將來〉、散文〈我的自畫像〉、〈吾友張星建氏側寫〉、〈檳榔籠〉、小說〈辣薤罐〉、〈憂鬱的詩人〉。<br>
                    1941年脫離《台灣文藝》，與黃得時、王井泉、陳逸松、中山侑等人組「啟文社」，創辦《台灣文學》，與西川滿主持之《文藝台灣》分庭抗禮。發表小說〈藝旦之家〉、〈論語與雞〉、〈部落的悲劇〉、散文〈酒是稚氣還是邪氣〉、〈媽祖〉、評論〈台灣文化的自我批判〉。      1942年發表小說〈夜猿〉、〈頓悟〉、〈閹雞〉、〈地方生活、散文〈無救的人們〉、〈地相學〉。十月與西川滿、濱島隼雄、龍瑛宗赴東京參加第一回「大東亞文學者大會」。<br>
                    1943年以小說〈夜猿〉與西川滿同獲頒皇民奉公會第一回台灣文學賞，並與王井泉、林博秋、呂赫若、呂泉生、楊三郎等百餘人組織「厚生演劇研究會」，於台北永樂座舉行「第一回研究發表會」，其中原作〈閹雞〉由林博秋改編為舞臺劇公演，在當時造成相當大的迴響。日據末期以作家職被徵召擔任皇民奉公會參議等職。十一月，《台灣文學》奉命廢刊。<br>
                    1944年出任台中州大里莊長，熱心地方政治。1946年當選第一屆台中縣參議員，1947年代理能高區署長。戰前可算是日文創作最豐者，戰後停止創作，228事件後漸離公職。<br>
                    1945 年戰後，張文環因親睹臺灣政治亂象及思想壓制，曾長期停止創作；張文環長子張孝宗指出：「光復以後，家父對政治多所失望，……家父對光復以後種種政治怪現象相當反感。」<br>
                    晚年利用業餘創作，1975年撰寫長篇小說《地に這うもの》在日本推出，獲日本圖書出版協會推薦為優良圖書，中譯本《滾地郎》由廖清秀先生翻譯，在台灣出版。<br>
                    1978年完成《從山上望見的街燈》初稿後，2月12日不幸因心臟病辭世，享年70歲。<br>
                    作品以小說、評論隨筆為主，並有譯作與數首新詩，整體數量相當可觀。2002年陳萬益編有8卷本《張文環全集》，由臺中縣立文化中心出版。<br>
                    評價<br>
                    在日治時期，臺灣作家的處境十分困難，因此要有非常的生存之道。日本學者野間信幸在〈張文環的文學活動及其特色〉中說，張文環在戰爭時期處理困境的模式有三：『第一，顧左右而言他。第二，以其人之道還治其人。第三，當機立斷，委曲求全。』<br>
                    彭瑞金曾說：『張文環本身可以說是謎一樣的作家。從日本人的角度來看，在他的作品裡難以找到反日的成分，反過來說，從台灣人的角度來看，也找不到他抗日的成分。張文環他具有寫實主義的風貌，但是卻不具批判性，而是傾向自然主義的客觀的呈現。』或許就是因為這樣，在他與西川滿的《文藝台灣》分庭抗禮之時，西川滿仍無法以他戰時的身分對其文學作品進行打壓。<br>
                    劉登翰也說過：『張文環是用寫實主義手法來描寫社會問題和家庭問題。對弱小人們的不幸遭遇寄予深厚的人道主義同情，對人性中的自私給與無情的揭露和鞭撻，但是這一切都在冷靜的描寫中進行。沒有賴和的尖銳指控，沒有楊逵的社會主義理念，也沒有吳濁流的冷笑，可是他透過他那寬闊的人道主義情懷，還是能夠讓人感受到他寫作所蘊含的寫實主義的力量。』<br>
                    葉石濤則說：「張文環文學的特質在於他濃厚的人道精神。他以為在這個世界裡，有些地方是連做人的條件都缺乏的，簡言之，他所關懷的是頑強地紮根於土地的農民，那被欺凌、被虐待的生活，剛好日據時代的殖民地台灣，正是此種缺乏做人條件的地方。」<br>

