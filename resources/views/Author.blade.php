            呂赫若，本名呂石堆，筆名呂赫若，生於日治台灣臺中廳葫蘆墩支廳潭仔墘區（今台中市潭子區）。呂為活躍於台灣日治時期到戰後初期的小說家，在戲劇、音樂與文學方面也頗有造詣，而被譽為台灣第一才子。呂石堆之所以用呂赫若為筆名，主要是擷取他敬佩的兩位作家，一個是中國的郭沫若，另一個則是朝鮮作家張赫宙，各取其中一字組合。<br>
            呂赫若出身地主家庭，從小接受新式教育與新式思想，畢業於台中師範學校，於1935年發表第一篇日文小說「牛車」而成為文壇矚目對象，被譽為「文學天才」。也是第一位作品被介紹到中國大陸的作家之一。<br>
            1939年呂赫若留學日本，研究聲樂。
